public class Categorias {

    int idCategoria, idTipoCategoria;
    String nomeCategoria, descricaoCategoria;
    Double latitudeCategoria, longitudeCategoria;
    int telefoneCategoria;
    String moradaCategoria;
    String imagemCategoria;

    public void setIdCategoria(int idCategoria) {
        this.idCategoria = idCategoria;
    }

    public int getIdCategoria() {
        return idCategoria;
    }

    public void setIdTipoCategoria(int idTipoCategoria) {
        this.idTipoCategoria = idTipoCategoria;
    }

    public int getIdTipoCategoria() {
        return idTipoCategoria;
    }

    public void setNomeCategoria(String nomeCategoria) {
        this.nomeCategoria = nomeCategoria;
    }

    public String getNomeCategoria() {
        return nomeCategoria;
    }

    public void setDescricaoCategoria(String descricaoCategoria) {
        this.descricaoCategoria = descricaoCategoria;
    }

    public String getDescricaoCategoria() {
        return descricaoCategoria;
    }

    public void setLatitudeCategoria(Double latitudeCategoria) {
        this.latitudeCategoria = latitudeCategoria;
    }

    public Double getLatitudeCategoria() {
        return latitudeCategoria;
    }

    public void setLongitudeCategoria(Double longitudeCategoria) {
        this.longitudeCategoria = longitudeCategoria;
    }

    public Double getLongitudeCategoria() {
        return longitudeCategoria;
    }

    public void setTelefoneCategoria(int telefoneCategoria) {
        this.telefoneCategoria = telefoneCategoria;
    }

    public int getTelefoneCategoria() {
        return telefoneCategoria;
    }

    public void setMoradaCategoria(String moradaCategoria) {
        this.moradaCategoria = moradaCategoria;
    }

    public String getMoradaCategoria() {
        return moradaCategoria;
    }

    public void setImagemCategoria(String imagemCategoria) {
        this.imagemCategoria = imagemCategoria;
    }

    public String getImagemCategoria() {
        return imagemCategoria;
    }
}
